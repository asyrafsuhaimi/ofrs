				<p align="justify"><b>SEJARAH SEKOLAH</b><br /><br />
				
				Sekolah Menengah Kebangsaan Bandar Tasik Kesuma atau ringkasannya SMKBTK , merupakan sebuah sekolah menengah yang terletak di Jalan Kesuma 7/1 , Bandar Tasik Kesuma.<br/><br/>


Sekolah ini telah ditubuhkan pada 2 Januari 2003. 3 Januari 2003 merupakan tarikh bersejarah bagi sekolah ini apabila majlis pendaftaran pelajar printis diadakan. pada peringkat permulaan sesi persekolahan, kemasukan pelajar baru hanya terdiri daripada Tingkatan peralihan dan Tingkatan Satu. Bagi memulakan sesi persekolahan 2003 yang bermula pada 6 Januari 2003, ia beroperasi dengan menumpang bangunan SK Bandar Tasik Kesuma kerana SMK Bandar Tasik Kesuma masih dalam proses pembinaan.</br></br>

Pada awal penubuhannya, bilangan pelajar yang mendaftar adalah seramai 142 orang iaitu 81 orang pelajar lelaki dan 61 orang pelajar perempuan dengan tenaga pengajar seramai 23 orang serta seorang staf sokongan. Komposisi pelajarnya terdiri daripada kaum Melayu seramai 85 orang, 55 orang pelajar India dan 2 orang pelajar Cina.<br/><br/>


Sebagai permulaan, sekolah ini beroperasi pada sesi pagi yang teridiri daripada 5 buah kelas sahaja iaitu 4 kelas tingkatan satu dan sebuah kelas tingkatan peralihan. Dengan komitmen para guru dan kerjasama ibu bapa serta kelengkapan fizikal sekolah diharapkan sekolah ini mampu bersaing dengan sekolah-sekolah lain di daerah ini bagi merealisasikan visi dan misi sekolah.<br/><br/>

Sekolah ini dinamakan sebagai Sekolah Menengan Kebangsaan Bandar Tasik Kesuma berdasarkan kepada lokasi sekolah ini yang terletak di kawasan ,Bandar Tasik Kesuma. Timbulnya nama Kesuma ini adalah lanjutan daripada minat pemacu kawasan ini kepada nama-nama bunga. Lantaran itu "KESUMA" menjadi pilihan kepada nama kawasan ini. Kesuma merupakan sejenis bunga hutan yang unik dan amat sukar dijumpai.<br/><br/>


Pada tahun 2009 , Sekolah Menengah Kebangsaan Bandar Tasik Kesuma memiliki 657 pelajar lelaki dan 619 pelajar perempuan , menjadikan jumlah keseluruhan 1276 orang. Ia mempunyai seramai 80 orang guru.<br/><br/>

					 

